#include <unistd.h>

#define SIZE 4

int	check(char *str)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	if (str[0] == '\0')
		j = 1;
	while (str[i] != '\0')
	{
		if (i % 2 != 0 && str[i] != ' ')
			j = 1;
		if (i % 2 == 0 && !(str[i] >= '1' && str[i] <= '0' + SIZE))
			j = 1;
		i++;
	}
	if (j == 1 || i != (SIZE * 8 - 1))
	{
		write(2, "Error\n", 6);
		return (1);
	}
	return (0);
}
