#include <unistd.h>

void	print_matriz(int matr[4][4])
{
	int	x;
	int	y;
	char	c;

	x = 0;
	y = 0;

	while (y < 4)
	{
		while (x < 4)
		{
			c = matr[x][y] + '0';
			write(1, &c, 1);
			if (x < 3)
			{
				write(1, " ", 1 );
			}
			x++;
		}
		write(1, "\n", 1);
		x = 0;
		y++;
	}
}

int	main(void)
{
	int	matriz[4][4] = {
		{1, 2, 3, 4},
		{2, 3, 4, 1},
		{3, 4, 1, 2},
		{4, 1, 2, 3}
	};

	print_matriz(matriz);
	return (0);
}	
